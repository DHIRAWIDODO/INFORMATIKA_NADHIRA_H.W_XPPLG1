adira = 10
bdira = 20
opdira = '/'

if (opdira == '+'):
    hadira = (adira + bdira)
elif (opdira == '-'):
    hadira = (adira - bdira)
elif (opdira == '*'):
    hadira = (adira * bdira)
elif (opdira == '/'):
    hadira = (adira / bdira)
else:
    print("Inputan salah")
print("Hasil =", hadira)